use clap::{Arg, App};
use std::fs;

fn main() {

    let matches = App::new("day1").arg(
        Arg::with_name("file").short("f").long("file").takes_value(true)
    ).get_matches();

    let file = matches.value_of("file").unwrap();
    let input = fs::read_to_string(file).unwrap();

    let lines: Vec<&str> = input.split('\n').filter(|s| !s.is_empty()).collect();

    let mut map: Vec<Vec<bool>> = vec![];
    for line in lines {
        let mut row: Vec<bool> = vec![];
        for char in line.chars() {
            let which = match char {
                '.' => false,
                '#' => true,
                _ => panic!()
            };
            row.push(which);
        }
        map.push(row);
    }

    let slopex = 3;

    let mut x = 0;
    let mut trees = 0;
    for y in 0..map.len() {
        if map[y][x % (map[y].len())] {
            // println!("{} {}", y+1 ,x % (map[y].len()));
            trees += 1;
        }
        x += slopex;
    }

    println!("Trees {}", trees);
}
